#include<delay.h>


/********************************************************************************************************
*  函数: delayms(unsigned int z)				                                                           
*  输入参数: z
*  输出参数: none                                  
*  功能: zms延时函数
********************************************************************************************************/
void delayms(unsigned int z)    //  z*1ms延时程序，11.0592MHz晶振
{
	unsigned int x,y;
	for(x=z;x>0;x--)
		for(y=800;y>0;y--);
}

/********************************************************************************************************
*  函数: delayms(unsigned int z)				                                                           
*  输入参数: z
*  输出参数: none                                  
*  功能: zms延时函数
********************************************************************************************************/
void delay_10us(unsigned int z)    //  z*10us延时程序，11.0592MHz晶振
{
	unsigned int x,y;
	for(x=z;x>0;x--)
		for(y=6;y>0;y--);
}
